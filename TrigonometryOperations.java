public class TrigonometryOperations {
    public static void main(String[] args) {
        // Convert degrees to radians
        double angle = Math.toRadians(45); // Converting 45 degrees to radians

        // Calculate sine
        double sine = Math.sin(angle);
        System.out.println("Sine of 45 degrees (in radians) is: " + sine);

        // Calculate cosine
        double cosine = Math.cos(angle);
        System.out.println("Cosine of 45 degrees (in radians) is: " + cosine);

        // Calculate tangent
        double tangent = Math.tan(angle);
        System.out.println("Tangent of 45 degrees (in radians) is: " + tangent);
    }
}

